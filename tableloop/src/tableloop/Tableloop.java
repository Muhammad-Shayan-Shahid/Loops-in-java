package tableloop;
public class Tableloop {
    public static void main(String[] args) {
        for(int i=2; i<=5 ;i++){
            for(int j=2; j<=5 ;j++){
            System.out.print(i+"X"+j+"="+(i*j)+" ");
        }
            System.out.println("");
        }
    }
    
}
